//! More information about node types.
//! Long term they should be gathered from other sources automatically.

use crate::types::Type;

impl Type {
    pub fn is_syntax(&self) -> bool {
        is!(
            self,
            HashInclude, // "#include",
            NewLine,     // "\n",
            HashDefine,  // "#define",
            LParen,      // "(",
            DotDotDot,   // "...",
            Comma,       // ",",
            RParen,      // ")",
            // HashIf, // "#if",
            // HashEndif, // "#endif",
            // HashIfdef, // "#ifdef",
            // HashIfndef, // "#ifndef",
            // HashElse, // "#else",
            // HashElif, // "#elif",
            Bang,      // "!",
            Tilde,     // "~",
            Dash,      // "-",
            Plus,      // "+",
            Star,      // "*",
            Slash,     // "/",
            Percent,   // "%",
            PipePipe,  // "||",
            AmpAmp,    // "&&",
            Pipe,      // "|",
            Caret,     // "^",
            Amp,       // "&",
            EqEq,      // "==",
            BangEq,    // "!=",
            GT,        // ">",
            GTEq,      // ">=",
            LTEq,      // "<=",
            LT,        // "<",
            LtLt,      // "<<",
            GtGt,      // ">>",
            SemiColon, // ";",
            // Typedef, // "typedef",
            Extern,     // "extern",
            TS1,        // "__attribute__",
            ColonColon, // "::",
            TS2,        // "[[",
            TS3,        // "]]",
            // TS4, // "__declspec",
            // TS5, // "__based",
            // TS6, // "__cdecl",
            // TS7, // "__clrcall",
            // TS8, // "__stdcall",
            // TS9, // "__fastcall",
            // TS10, // "__thiscall",
            // TS11, // "__vectorcall",
            // MsRestrictModifier, // "ms_restrict_modifier",
            // MsUnsignedPtrModifier, // "ms_unsigned_ptr_modifier",
            // MsSignedPtrModifier, // "ms_signed_ptr_modifier",
            // TS12, // "_unaligned",
            // TS13, // "__unaligned",
            LBrace,   // "{",
            RBrace,   // "}",
            LBracket, // "[",
            RBracket, // "]",
            Eq,       // "=",
            // Static, // "static",
            // Register, // "register",
            // Inline, // "inline",
            // ThreadLocal, // "thread_local",
            // Const, // "const",
            // Volatile, // "volatile",
            // Restrict, // "restrict",
            // TS14, // "_Atomic",
            // Mutable, // "mutable",
            // Constexpr, // "constexpr",
            // Constinit, // "constinit",
            // Consteval, // "consteval",
            // Signed, // "signed",
            // Unsigned, // "unsigned",
            // Long, // "long",
            // Short, // "short",
            Enum,    // "enum",
            Struct,  // "struct",
            Union,   // "union",
            Colon,   // ":",
            If,      // "if",
            Else,    // "else",
            Switch,  // "switch",
            Case,    // "case",
            Default, // "default",
            While,   // "while",
            Do,      // "do",
            For,     // "for",
            Return,  // "return",
            // Break, // "break",
            // Continue, // "continue",
            // Goto, // "goto",
            QMark, // "?",
            // StarEq, // "*=",
            // SlashEq, // "/=",
            // PercentEq, // "%=",
            // PlusEq, // "+=",
            // DashEq, // "-=",
            // LtLtEq, // "<<=",
            // GtGtEq, // ">>=",
            // AmpEq, // "&=",
            // CaretEq, // "^=",
            // PipeEq, // "|=",
            // AndEq, // "and_eq",
            // OrEq, // "or_eq",
            // XorEq, // "xor_eq",
            // Not, // "not",
            // Compl, // "compl",
            // TS15, // "<=>",
            // Or, // "or",
            // And, // "and",
            // Bitor, // "bitor",
            // Xor, // "xor",
            // Bitand, // "bitand",
            // NotEq, // "not_eq",
            // DashDash, // "--",
            // PlusPlus, // "++",
            // Sizeof, // "sizeof",
            // Asm, // "asm",
            Dot,    // ".",
            DashGt, // "->",
        )
    }

    pub fn is_error(&self) -> bool {
        self == &Type::ERROR || self == &Type::_ERROR
    }
}
macro_rules! is {
    ($e:expr, $($p:ident $(if $guard:expr)?, )*) => {
        match $e {$(
            Type::$p $(if $guard)? => true,)*
            _ => false
        }
    };
}

pub(super) use is;
